    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Comida comida = new Comida();
        comida.setNome(jLNome.getText());
        comida.setQuantidade(Integer.parseInt(jLQdte.getText()));
        comida.setTipo(jLTipo.getText());
        comida.setDataValidade(new Date(jLDataValidade.getText()));
   
        try {
            ComidaDAO.insert(comida);
            JOptionPane.showMessageDialog(null, "Comida cadastrada com sucesso");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao inserir!");
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

