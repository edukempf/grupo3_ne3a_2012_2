    private String validaCampos() {
        StringBuilder erros = new StringBuilder("");
        this.setColorBG();
        if (jTextFieldCapacidade.getText() == null || jTextFieldCapacidade.getText().equals("")) {
            erros.append("O campo Capacidade é obrigatório!\n");
            jTextFieldCapacidade.setBackground(Color.red);
        }
        return erros.toString();
    }

