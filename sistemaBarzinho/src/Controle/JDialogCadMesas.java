    private Mesa getDadosDosCampos() {
        Mesa mes = new Mesa();
        mes.setCapacidade(Integer.parseInt(jTextFieldCapacidade.getText()));
        mes.setStatus(Object(jRadioButtonL.getSelectedObjects()));
        if (this.mes != null) {
            mes.setId(this.mes.getId());
        }

        return mes;
    }

