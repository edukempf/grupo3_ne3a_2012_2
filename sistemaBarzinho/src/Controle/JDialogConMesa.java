    private void criaTabela() {
        model = new DefaultTableModel(new String[]{"id", "capacidade", "status"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTable1.setModel(model);
    }

    private void preenchetabela() {

        model.setNumRows(0);
        try {
            ArrayList<Mesa> lista;
            if (!jTextFieldNome.getText().equals("")) {
                lista = (ArrayList<Mesa>) dao.buscaMesa(Integer.parseInt(jTextFieldNome.getText()));

            } else {
                lista = (ArrayList<Mesa>) dao.lista();
            }
            for (Mesa mesa : lista) {
                String status;
                if (mesa.isStatus()) {
                    status = "Ocupada";
                } else {
                    status = "Livre";
                }
                model.addRow(new Object[]{mesa.getId(),
                    mesa.getCapacidade(),
                    status});

            }
            jTable1.updateUI();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

