    private Mesa pegaMesaSelecionada() {
        daoMesa = new MesaDAO();
        Mesa aux = daoMesa.get(Integer.parseInt(jComboBoxMesa.getSelectedItem().toString().substring(jComboBoxMesa.getSelectedItem().toString().lastIndexOf(" ") + 1, jComboBoxMesa.getSelectedItem().toString().length())));
        return aux;
    }

    private void fechaMesa() {
        if (jComboBoxMesa.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione a mesa que deseja abrir!");
        } else {
            daoMesa = new MesaDAO();
            daoPBeb = new PedidoBebidaDAO();
            daoPPra = new PedidoPratoDAO();
            Mesa mesaSelecionada = pegaMesaSelecionada();
            List listaPedidoBebidas = daoPBeb.buscaPedidoAbertoPorMesa(mesaSelecionada.getId());
            List listaPedidoPratos = daoPPra.buscaPedidoAbertoPorMesa(mesaSelecionada.getId());
            System.out.println(listaPedidoBebidas.size() + listaPedidoPratos.size());
            if ((listaPedidoBebidas.size() + listaPedidoPratos.size()) == 0) {
                mesaSelecionada.setStatus(false);
                try {
                    TransactionManager.beginTransaction();
                    daoMesa.persisteObjeto(mesaSelecionada);
                    TransactionManager.comitTransaction();
                    JOptionPane.showMessageDialog(null, "Mesa fechada com sucesso");
                    preencheComboBoxMesa();
                } catch (Exception ex) {
                    ex.printStackTrace();
                    TransactionManager.rollbackTransaction();
                    JOptionPane.showMessageDialog(null, "Erro ao fechar mesa!");
                }
            }else{
                int op=JOptionPane.showConfirmDialog(null, "Existem pedidos que ainda n√£o foram pagos nessa mesa, desejar realizar o pagamento agora?");
                if(op==JOptionPane.YES_OPTION){ 
                    new JDialogCalcularConta(null, true).setVisible(true);
                }
            }
        }
    }

    private void preencheComboBoxMesa() {
        daoMesa = new MesaDAO();
        List<Mesa> mesas = daoMesa.buscaMesaOcupada();
        List<String> mesasOcupadas = new ArrayList<String>();
        mesasOcupadas.add("Selecione uma mesa");
        for (Mesa mesa : mesas) {
            mesasOcupadas.add("Mesa - " + mesa.getId());
        }
        modelCombo = new DefaultComboBoxModel(mesasOcupadas.toArray());
        jComboBoxMesa.setModel(modelCombo);
        jComboBoxMesa.updateUI();
    }

